public class Object {
    String DefinedName=null;
    int Line=0;
    String ClassNameL=null;
    String ClassNameR=null;

    public String getDefinedName() {
        return DefinedName;
    }

    public void setDefinedName(String definedName) {
        DefinedName = definedName;
    }

    public int getLine() {
        return Line;
    }

    public void setLine(int line) {
        Line = line;
    }

    public String getClassNameL() {
        return ClassNameL;
    }

    public void setClassNameL(String classNameL) {
        ClassNameL = classNameL;
    }

    public String getClassNameR() {
        return ClassNameR;
    }

    public void setClassNameR(String classNameR) {
        ClassNameR = classNameR;
    }
}
